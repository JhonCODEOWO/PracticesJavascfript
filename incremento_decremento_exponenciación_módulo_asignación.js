//Incremento
let numero1 = 7;
numero1++;

console.log(numero1);

//Decremento
let numero2 = 7;
numero1--;

console.log(numero1);

//Operadores de asignación
let numero3 = 7;
numero1 += 10; //Se usa para evitar numero1 = numero1 + 10

let numero4 = 7;
numero2 -= 50;

console.log(numero3);
console.log(numero4);
//NOTA: Igual se pueden utilizar otros operadores con este mismo estilo de asignación

//Exponenciación
let exponente = 2**2;

console.log(exponente);

//Operador módulo
let modulo = 10 % 3;

console.log(modulo);

